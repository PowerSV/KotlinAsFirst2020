fun isPalindrome(n: Int): Boolean {
    var a = n
    var tf = true
    while (a >= 10) {
        if (a % 10 != a / 10.0.pow(digitNumber(a) - 1).toInt()) {
            tf = false
            break
        }
        a = a % (10.0.pow(digitNumber(a) - 1)).toInt() / 10
    }
    return tf
}

