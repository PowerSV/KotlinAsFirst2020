 * Взаимно простые числа не имеют общих делителей, кроме 1.
 * Например, 25 и 49 взаимно простые, а 6 и 8 -- нет.
 */
fun isCoPrime(m: Int, n: Int): Boolean = greatestCommonFactor(m, n) == 1
fun greatestCommonFactor(m: Int, n: Int): Int {
    var x = m
    var y = n
    while (x != 0 && y != 0) {
        if (x > y)
            x %= y
        else y %= x
    }
    return x + y
}

